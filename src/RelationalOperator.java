public class RelationalOperator {
    public static void main(String[] args){
        int a = 10;
        int b = 12;
        System.out.println("a等于b:"+(a==b));
        System.out.println("a不等于b:"+(a!=b));
        System.out.println("a大于等于b:"+(a>=b));
        System.out.println("a小于等于b:"+(a<=b));
    }
}
